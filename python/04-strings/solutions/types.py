def check_isalpha(s: str) -> bool:
    """Return True if all characters are alphabetic using isalpha()."""
    return s.isalpha()


def check_isalnum(s: str) -> bool:
    """Return True if all characters are alphanumeric using isalnum()."""
    return s.isalnum()


def check_isdigit(s: str) -> bool:
    """Return True if all characters are digits using isdigit()."""
    return s.isdigit()


def check_isnumeric(s: str) -> bool:
    """Return True if all characters are numeric using isnumeric()."""
    return s.isnumeric()


def check_islower(s: str) -> bool:
    """Return True if all cased characters are lowercase using islower()."""
    return s.islower()


def check_isupper(s: str) -> bool:
    """Return True if all cased characters are uppercase using isupper()."""
    return s.isupper()


def check_istitle(s: str) -> bool:
    """Return True if the string is title-cased using istitle()."""
    return s.istitle()


def check_isspace(s: str) -> bool:
    """Return True if all characters are whitespace using isspace()."""
    return s.isspace()
